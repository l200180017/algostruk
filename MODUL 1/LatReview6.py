def tigaataulima(x):
    if (x %3 == 0 and x % 5 == 0):
        print("bilangan itu adalah kelipatan tiga atau lima")
    elif (x %3 == 0):
        print("bilangan itu adalah kelipatan tiga")
    elif (x %5 == 0):
        print("bilangan itu adalah kelipatan lima")
    else:
        print("bilangan itu bukan kelipatan tiga maupun lima")
